    $mail->SMTPAuth   = true;                                   // Enable SMTP authentication
    $mail->Username   = 'nelsonmouatvergara@gmail.com';                     // SMTP username
    $mail->Password   = 'nelson29101998';                               // SMTP password
    $mail->SMTPSecure = PHPMailer::ENCRYPTION_STARTTLS;         // Enable TLS encryption; `PHPMailer::ENCRYPTION_SMTPS` encouraged
    $mail->Port       = 587;                                    // TCP port to connect to, use 465 for `PHPMailer::ENCRYPTION_SMTPS` above

    //Recipients
    $mail->setFrom('nelsonmouatvergara@gmail.com', 'EducaciÃ³n Finanziera');
    $mail->addAddress('nelsonmouatvergara@gmail.com',); 

    // Content
    $mail->isHTML(true);                                  // Set email format to HTML
    $mail->Subject = 'BIENVENIDO a PHPMailer';
    $mail->Body    = 'Hola a todo <b>in bold!</b>';

    $mail->send();
    echo 'Mensaje enviado';
} catch (Exception $e) {
    echo "Message could not be sent. Mailer Error: {$mail->ErrorInfo}";
}
